export const tiers = ["Operational", "Process-Level", "Strategic"];

function r(
  riskNumber,
  department,
  category,
  title,
  description,
  context,
  owner,
  tier,
  likelihood,
  impact,
  controlDescription,
  controlAttributes,
  controlEffectiveness,
  mitigationPlan,
  mitigationOwner,
  mitigationTimeline,
) {
  const macroCategory =
    departments.find((d) => d.name === department)?.macroCategory ?? "—";
  const inherentRating = likelihood * impact;
  const residualRating = inherentRating * controlEffectiveness;
  return {
    id: riskNumber,
    riskNumber,
    department,
    macroCategory,
    category,
    title,
    description,
    context,
    owner,
    tier,
    likelihood,
    impact,
    inherentRating,
    controlDescription,
    controlAttributes,
    controlEffectiveness,
    residualRating,
    mitigationPlan,
    mitigationOwner,
    mitigationTimeline,
  };
}

